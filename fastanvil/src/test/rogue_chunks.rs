fn extract_heights(chunk: &mut Chunk) -> [usize; 256] {
    let mut heights = [0; 256];
    for z in 0..16 {
        for x in 0..16 {
            heights[z * 16 + x] = chunk.height_of(x, z).unwrap();
        }
    }
    heights
}

